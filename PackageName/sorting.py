def bubble_sort(items):
    '''Return array of items, sorted in ascending order.

    Argument:
        items (array): an array of numbers.

    Returns:
        array: items sorted in ascending order.

    Examples:
        >>> bubble_sort([5,4,3,2,1])
        [1,2,3,4,5]
        >>> bubble_sort([1,3,2])
        [1,2,3]
        >>> bubble_sort([1,5,3,9,7,4])
        [1,3,4,5,7,9]
    '''
    return sorted(items)

